/* ===== Пиксельное сердце ===== */
function drawPixelHeart() {
  const pixelSize = 10;
  const heart = [
    "00111100111100",
    "01111111111110",
    "11111111111111",
    "11111111111111",
    "11111111111111",
    "01111111111110",
    "00111111111100",
    "00011111111000",
    "00001111110000",
    "00000111100000",
    "00000011000000"
  ];

  let pixels = [];
  heart.forEach((row, y) => {
    row.split("").forEach((cell, x) => {
      if (cell === "1") pixels.push({ x, y });
    });
  });

  // Размер сердца
  const heartWidth = heart[0].length * pixelSize;
  const heartHeight = heart.length * pixelSize;

  // Координаты верхнего левого угла для центрирования
  const offsetX = (canvas.width - heartWidth) / 2;
  const offsetY = (canvas.height - heartHeight) / 2;

  let i = 0;
  const interval = setInterval(() => {
    if (i >= pixels.length) {
      clearInterval(interval);
      // Надпись имени моей любимой девочки по центру
      ctx.fillStyle = "white";
      ctx.font = "20px monospace";
      ctx.textAlign = "center";
      ctx.textBaseline = "middle";
      ctx.fillText("Ника", canvas.width / 2, canvas.height / 2);
      return;
    }

    const p = pixels[i];
    ctx.fillStyle = "#ff2d75";
    ctx.fillRect(
      p.x * pixelSize + offsetX,
      p.y * pixelSize + offsetY,
      pixelSize,
      pixelSize
    );

    i++;
  }, 30);
}
