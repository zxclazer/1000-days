/* ===== Обновление карточки ===== */
function update(direction = 1) {
  if (current === totalDays) {
    card.style.display = "none"; 
    finalScreen.classList.remove("hidden");
    return;
  }

  card.style.transform = `translateX(${direction * 60}px)`;
  card.style.opacity = 0;

  setTimeout(() => {
    photo.src = `photos/${current}.webp`;
    caption.textContent = texts[current] || `День ${current}`;
    counter.textContent = `День ${current} / ${totalDays}`;
    preloadImage(current + 1);

    card.style.transform = "translateX(0)";
    card.style.opacity = 1;
  }, 200);
}

